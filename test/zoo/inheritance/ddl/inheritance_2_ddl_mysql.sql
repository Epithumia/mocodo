CREATE DATABASE IF NOT EXISTS inheritance;
USE inheritance;

CREATE TABLE ALIQUET (
  magna VARCHAR(42) NOT NULL,
  tellus VARCHAR(42) NOT NULL,
  type TINYINT UNSIGNED NOT NULL,
  PRIMARY KEY (magna, tellus)
) ENGINE=InnoDB DEFAULT CHARSET=UTF8MB4;

CREATE TABLE CONSEQUAT (
  fermentum VARCHAR(42) NOT NULL,
  dederit VARCHAR(42),
  PRIMARY KEY (fermentum)
) ENGINE=InnoDB DEFAULT CHARSET=UTF8MB4;

CREATE TABLE CURABITUR (
  gravida VARCHAR(42) NOT NULL,
  amor VARCHAR(42),
  PRIMARY KEY (gravida)
) ENGINE=InnoDB DEFAULT CHARSET=UTF8MB4;

CREATE TABLE DIGNISSIM (
  tellus VARCHAR(42) NOT NULL,
  terra VARCHAR(42),
  PRIMARY KEY (tellus)
) ENGINE=InnoDB DEFAULT CHARSET=UTF8MB4;

CREATE TABLE LACUS (
  magna VARCHAR(42) NOT NULL,
  vestibulum VARCHAR(42),
  fermentum VARCHAR(42) NOT NULL,
  tempor VARCHAR(42),
  fugit VARCHAR(42),
  PRIMARY KEY (magna)
) ENGINE=InnoDB DEFAULT CHARSET=UTF8MB4;

CREATE TABLE LIBERO (
  posuere VARCHAR(42) NOT NULL,
  lacrima VARCHAR(42),
  PRIMARY KEY (posuere)
) ENGINE=InnoDB DEFAULT CHARSET=UTF8MB4;

CREATE TABLE NEC (
  magna VARCHAR(42) NOT NULL,
  vestibulum VARCHAR(42),
  fermentum VARCHAR(42) NOT NULL,
  pulvinar VARCHAR(42),
  audis VARCHAR(42),
  gravida VARCHAR(42) NOT NULL,
  PRIMARY KEY (magna)
) ENGINE=InnoDB DEFAULT CHARSET=UTF8MB4;

CREATE TABLE QUAM (
  cras VARCHAR(42) NOT NULL,
  sed VARCHAR(42),
  magna VARCHAR(42) NOT NULL,
  PRIMARY KEY (cras)
) ENGINE=InnoDB DEFAULT CHARSET=UTF8MB4;

CREATE TABLE SODALES (
  magna VARCHAR(42) NOT NULL,
  vestibulum VARCHAR(42),
  fermentum VARCHAR(42) NOT NULL,
  convallis VARCHAR(42),
  ipsum VARCHAR(42),
  PRIMARY KEY (magna)
) ENGINE=InnoDB DEFAULT CHARSET=UTF8MB4;

CREATE TABLE SUSCIPIT (
  orci VARCHAR(42) NOT NULL,
  lorem VARCHAR(42),
  magna VARCHAR(42) NOT NULL,
  type TINYINT UNSIGNED NOT NULL,
  PRIMARY KEY (orci)
) ENGINE=InnoDB DEFAULT CHARSET=UTF8MB4;

CREATE TABLE ULTRICES (
  posuere VARCHAR(42) NOT NULL,
  magna VARCHAR(42) NOT NULL,
  PRIMARY KEY (posuere, magna)
) ENGINE=InnoDB DEFAULT CHARSET=UTF8MB4;

